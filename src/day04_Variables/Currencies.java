package day04_Variables;

public class Currencies {
    public static void main(String[] args) {

        int dollar = 1000;

        //jpy, pound, peso, cad, riyalXX
        double lira = dollar * 9.53;
        double euro = dollar * 1.16;
        double peso = dollar * 2.35;
        double cad = dollar * 7.62;
        double jpy = dollar * 142.96;
        double pound = dollar * 0.87;
        double dinar = dollar * 1.16;

        System.out.println(dollar+ "dolar = " + lira+ "lira");
        System.out.println(dollar+ "dolar = " + euro+ "euro");
        System.out.println(dollar+ "dolar = " + peso+ "peso");
        System.out.println(dollar+ "dolar = " + cad+ "cad");
        System.out.println(dollar+ "dolar = " + jpy+ "jpy");
        System.out.println(dollar+ "dolar = " + pound+ "pound");
        System.out.println(dollar+ "dolar = " + dinar+ "dinar");









    }



}
