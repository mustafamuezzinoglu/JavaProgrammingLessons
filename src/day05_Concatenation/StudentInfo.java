package day05_Concatenation;

public class StudentInfo {
    /*
    Create a class called StudentInfo, and declare variables for the followings:
    			1. student name (String)
    			2. age (int)
    			3. gender (char)
    			4. school name (String)
    			5. phone number (String) (###)-###-####
    			6. full time (boolean)
     */
    public static void main(String[] args) {

        String student_name = "mustafa";
        int age = 45;
        char gender = 'M';
        String schoolName = "cydeo";
        String phone_number = "+90_(505)_366_46_77";
        boolean isFull_time = true;
        double GPA = 3.5;

        System.out.println("student_name = " + student_name);
        System.out.println("age = " + age);
        System.out.println("gender = " + gender);
        System.out.println("schoolName = " + schoolName);
        System.out.println("phone_number = " + phone_number);

        System.out.println("GPA = " + GPA);
        System.out.println("isFull_time = " + isFull_time);


    }






}
