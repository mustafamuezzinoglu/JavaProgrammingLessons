package day03_EscapeSequences;
/*
    escape sequences: MUST be given with double quote
    \n: starts a new line
    \t: Pparagraph space (tab)
    \\: single backslash
    \": double quote

     */
public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Java \nPython \nC#");
        System.out.println("----------------------------------------------------------------------------");

        System.out.println("Hello Cydeo \nHow are you all today? \nIt's nice to see you all! \nWhat class do we have nextweek");

        System.out.println("------------------------------------------------");

        System.out.println("\tJava is Cool Programming Language");

        System.out.println("------------------------------------------------");

        System.out.println("/ \\"); // in order to  print one backward slash we need to give two backward slashes

        System.out.println("------------------------------------------------");

        System.out.println("My faavorite Tv show is \"Game of Throne\""); //vin order to print double quote, we need to use \"

    }


}
