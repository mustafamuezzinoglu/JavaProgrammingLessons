public class MyCalculatorV1 {

    public int plus(int a,int b){
        return a+b;
    }
    public int minus(int a,int b){
        return a-b;
    }
    public int multiply(int a,int b){
        return a*b;
    }
    public double divide(double a,int b){
        return a/b;
    }


}
