package day17_WhileDoWhile;

public class WhileLoopIntro {
    public static void main(String[] args) {
        if(true){
            System.out.println("Wooden Spoon");//1 time
        }
        System.out.println("-----------------------------");

        while(true){ //repeated if
            System.out.println("Wooden Spoon"); //multiple times
        }






    }
}
